package package_javabasics;

public class ForLoop_Assignment {

	public static void main(String[] args) {
		// Print only odd number
		for(int i=1;i<=100;i++)
		{
			System.out.println(i);
			i++;
		}
		
		// Print only even number
		for(int j=2;j<=100;j++)
		{
			System.out.println(j);
			j++;
		}
		//print from -51 to 0
		for(int i=-51;i<=0;i++)
		{
			System.out.println(i);
		}
		
		//print from -4 to -10
				for(int i=-4;i>=-10;i--)
				{
					System.out.println(i);					
				}
		//print from 101 to 111
				for(int i=101;i<112;i++)
				{
					System.out.println(i);					
				}
		

	}

}
