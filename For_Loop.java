package package_javabasics;

public class For_Loop {

	public static void main(String[] args) {
		// Write program of for loop priting from 1-10
		
		for(int i=1;i<=10;i++)
		{
			System.out.println(i);
		}
		
		// Write program of for loop printing from 11-26
		for(int j=11;j<26;j++)
		{
			System.out.println(j);
		}
		
		// Write program of for loop priting from -1 to 0
		for(int k=-1;k<=0;k++)
		{
			System.out.println(k);
		}
        
		for(int m=-1;m>0;m++)
		{
			System.out.println(m);
		}
		

	}

}
