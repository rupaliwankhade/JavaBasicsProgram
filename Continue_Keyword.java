package package_javabasics;

public class Continue_Keyword {

	public static void main(String[] args) {
		// Continue keyword-skip the iteration
		
		//12th class assignment
		for(int i=1;i<=31;i++)
		{
			
			if(i==4)
			{
				continue;
			}
			if(i==17)
			{
				continue;
			}
			if(i==28)
			{
				continue;
			}
			System.out.println(i);
		}
		

	}

}
